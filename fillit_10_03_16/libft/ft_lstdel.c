#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *temp;

	while (*alst)
	{
		temp = *alst;
		ft_lstdelone(&temp, del);
		*alst = (*alst)->next;
	}
}
