#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list *new;

	new = (t_list*)malloc(sizeof(new) * content_size);
	if (!new)
		return (NULL);
	if (!content)
	{
		new->content = NULL;
		new->content_size = 0;
		new->next = NULL;
	}
	else if (content)
	{
		new->content = (void*)content;
		new->content_size = content_size;
		new->next = NULL;
	}
	return (new);
}
