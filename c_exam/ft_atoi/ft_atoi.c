#include <unistd.h>

int	ft_atoi(char *s)
{
	long aux;
	int i;
	int sign;
	int nb;

	i = 0;
	nb = 0;
	sign = 1;
	while (s[i] != '\t' || s[i] != ' ' || s[i] != '\n')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + (s[i] - '0');
		i++;
	}
	aux = nb;
	return (aux * sign);
}
